# first variable
var1 = 10

# Second variable
var2 = 20

# printing values of first and second variables before swapping
print('Before Swap :', '1st var:', var1, '2nd var:', var2)


# swapping variables using python trick
var1, var2 = var2, var1

print('After Swap :', '1st var:', var1, '2nd var:', var2)
